package curso.g2.operadores;

public class Contadores {

	public static void main(String[] args) {
		int valor =0;
		valor =23;
		valor +=10;
		System.out.println("valor "+ valor);
		System.out.println("valor "+ --valor);
		System.out.println("valor "+ ++valor);
		System.out.println("valor "+valor++);
		System.out.println("valor "+valor--);

	}

}
