package curso.g2.condicionales;

public class DibujarCuadrado {

	public static void main(String[] args) {
		for(int f=0;f<8;f++) {
			//imprimir * por filas
			for(int c=0;c<8;c++) {
				System.out.print(" * ");
			}
			//salto de lineas para imprimir en la siguiente linea
			System.out.println();

		}

	}

}
